#. Use capitalize(), title(), swapcase() methods to format the value of the string Coding For All.

company = "Coding For All"

# Capitalize -> only first character uppercase, rest lowercase
print("capitalize():", company.capitalize())

# Title -> each word starts with uppercase
print("title():", company.title())

# Swapcase -> lowercase becomes uppercase & uppercase becomes lowercase
print("swapcase():", company.swapcase())
